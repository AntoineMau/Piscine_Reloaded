int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		nb_elem;

	i = -1;
	nb_elem = 0;
	while (tab[++i])
		if (f(tab[i]) == 1)
			nb_elem++;
	return (nb_elem);
}
